package JOOP_01_1_Working_with_Abstraction_Lab.P02_Point_in_Rectangle;

public class Point {
    private int x;
    private int y;


    public Point() {
    }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
